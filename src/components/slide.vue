<template>
  <div class="slide" ref="slide">
    <div class="slide-group" ref="slideGroup">
      <slot>
      </slot>
    </div>
  </div>
</template>

<script>

  import { addClass } from '@/assets/js/dom'
  import BScroll from 'better-scroll'

  const COMPONENT_NAME = 'slide'

  export default {
    name: COMPONENT_NAME,
    props: {
      loop: {
        type: Boolean,
        default: false
      },
      autoPlay: {
        type: Boolean,
        default: false
      },
      interval: {
        type: Number,
        default: 4000
      },
      showDot: {
        type: Boolean,
        default: true
      },
      click: {
        type: Boolean,
        default: true
      },
      data:{
        type: Array,
        default: []
      },
      pageIndex:{
        type: Number,
        default: 1
      }
    },  
    data() {
      return {
        dots: [],
        currentPageIndex: 0
      }
    },
    watch:{
      data(){
        setTimeout(() => {
          this.refresh()
        }, 20)    
      },
      pageIndex(){
        console.log(this.pageIndex)
        try{
          this.slide.goToPage(this.pageIndex, 0, 0)
        }catch(e){

        }
        
      }
    },
    mounted() {
      setTimeout(() => {
        this._setSlideWidth()
        // if (this.showDot) {
        //   this._initDots()
        // }
        this._initSlide()

        if (this.autoPlay) {
          this._play()
        }
      }, 20)

      // window.addEventListener('resize', () => {
      //   if (!this.slide || !this.slide.enabled) {
      //     return
      //   }
      //   clearTimeout(this.resizeTimer)
      //   this.resizeTimer = setTimeout(() => {
      //     if (this.slide.isInTransition) {
      //       this._onScrollEnd()
      //     } else {
      //       if (this.autoPlay) {
      //         this._play()
      //       }
      //     }
      //     this.refresh()
      //   }, 60)
      // })
    },
    activated() {
      // keep-alive 激活时调用
      if (!this.slide) {
        return
      }
      this.slide.enable()
      let pageIndex = this.slide.getCurrentPage().pageX
      if (pageIndex > this.dots.length) {
        pageIndex = pageIndex % this.dots.length
      }
      this.slide.goToPage(pageIndex, 0, 0)
      if (this.loop) {
        pageIndex -= 1
      }
      this.currentPageIndex = pageIndex
      if (this.autoPlay) {
        this._play()
      }
    },
    deactivated() {
      // keep-alive 停用时调用
      this.slide.disable()
      clearTimeout(this.timer)
    },
    beforeDestroy() {
      this.slide.disable()
      clearTimeout(this.timer)
    },
    methods: {
      refresh() {
        this._setSlideWidth(true)
        this.slide.refresh()
        try {
          this.slide.goToPage(this.pageIndex, 0, 0)
        } catch (error) {
          
        }
        
      },
      next() {
        this.slide.next()
      },
      _setSlideWidth(isResize) {
        this.children = this.$refs.slideGroup.children

        let width = 0
        let slideHeight = document.documentElement.clientHeight
        let slideWidth = document.documentElement.clientWidth        
        for (let i = 0; i < this.children.length; i++) {
          let child = this.children[i]
          addClass(child, 'slide-item')

          child.style.width = slideWidth + 'px'
          child.style.height = slideHeight + 'px'

          width += slideWidth
        }
        if (this.loop && !isResize) {
          width += 2 * slideWidth
        }
        this.$refs.slideGroup.style.width = width + 'px'
      },
      _initSlide() {
        this.slide = new BScroll(this.$refs.slide, {
          scrollX: true,
          momentum: false,
          snap: {
            loop: this.loop,
            threshold: 0.3,
            speed: 400
          },
          click: this.click
        })        
        this.slide.on('scrollEnd', this._onScrollEnd)

        // this.slide.on('touchend', () => {
        //   if (this.autoPlay) {
        //     this._play()
        //   }
        // })

        // this.slide.on('beforeScrollStart', () => {
        //   if (this.autoPlay) {
        //     clearTimeout(this.timer)
        //   }
        // })
      },
      _onScrollEnd() {
        let pageIndex = this.slide.getCurrentPage().pageX
        if (this.loop) {
          pageIndex -= 1
        }
        this.currentPageIndex = pageIndex
        this.$emit('getPageIndex',pageIndex)
        if (this.autoPlay) {
          this._play()
        }
      },
      _initDots() {
        this.dots = new Array(this.children.length)
      },
      _play() {
        let pageIndex = this.slide.getCurrentPage().pageX + 1
        clearTimeout(this.timer)
        this.timer = setTimeout(() => {
          this.slide.goToPage(pageIndex, 0, 400)
        }, this.interval)
      }
    }
  }
</script>

